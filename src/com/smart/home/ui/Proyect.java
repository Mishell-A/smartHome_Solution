    private void btnVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVActionPerformed
        String encargadoSeleccionado = cmbE.getSelectedItem().toString();

    if (encargadoSeleccionado == null || encargadoSeleccionado.equals("")) {
        JOptionPane.showMessageDialog(null, "Seleccione un encargado primero.");
        return;
    }

    // Crear una lista temporal para los resultados
    ArrayList<Proyectos> filtrados = new ArrayList<>();

    for (Proyectos p : Lis) {
        if (p.getEncargado().equalsIgnoreCase(encargadoSeleccionado)) {
            filtrados.add(p);
        }
    }

    if (filtrados.isEmpty()) {
        JOptionPane.showMessageDialog(null, "No hay proyectos de este encargado.");
        return;
    }

    // Crear la matriz para mostrar en la tabla
    String Mat[][] = new String[filtrados.size()][6];
    for (int i = 0; i < filtrados.size(); i++) {
        Proyectos aux = filtrados.get(i);
        Mat[i][0] = Integer.toString(aux.getID());
        Mat[i][1] = aux.getNombre();
        Mat[i][2] = aux.getEncargado();
        Mat[i][3] = aux.getEstado();
        Mat[i][4] = aux.getFecha();
        Mat[i][5] = aux.getFin();
    }

    // Mostrar los resultados filtrados
    tblT.setModel(new javax.swing.table.DefaultTableModel(
        Mat,
        new String [] {
            "ID", "Nombre", "Encargado", "Estado", "Fecha Inicio", "Fecha Fin"
        }
    ));
    }//GEN-LAST:event_btnVActionPerformed

    private void btnBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBActionPerformed
         String nombreBuscado = JOptionPane.showInputDialog("Ingrese el nombre del proyecto:");

    if (nombreBuscado == null || nombreBuscado.trim().equals("")) {
        return; // Cancelado o vac√≠o
    }

    boolean encontrado = false;

    for (Proyectos p : Lis) {
        if (p.getNombre().equalsIgnoreCase(nombreBuscado.trim())) {
            // Mostrar la informaci√≥n encontrada
            JOptionPane.showMessageDialog(null,
                " Proyecto encontrado:\n\n" +
                "ID: " + p.getID() + "\n" +
                "Nombre: " + p.getNombre() + "\n" +
                "Encargado: " + p.getEncargado() + "\n" +
                "Estado: " + p.getEstado() + "\n" +
                "Fecha Inicio: " + p.getFecha() + "\n" +
                "Fecha Fin: " + p.getFin()
            );
            encontrado = true;
            break;
        }
    }

    if (!encontrado) {
        JOptionPane.showMessageDialog(null, "No se encontr√≥ ning√∫n proyecto con ese nombre.");
    }
    }//GEN-LAST:event_btnBActionPerformed


